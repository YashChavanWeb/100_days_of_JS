// Syntax: for loop
// for (1. initialize; 2. condition; 3. update)

// order of execution is : 1  2  3  , then goes on for 2 and 3 until condition becomes false





// Example

console.log('Program Started')

for (let i = 1; i <= 5; i++) {
    console.log(i)
}

console.log('Program Ended')






// Practice Question - Print all the even numbers from 0 to 20

for (let i = 0; i <= 20; i++) {
    if (i % 2 == 0)
        console.log(i)
}



























