// do while loop:
// it will by default run at-least once no matter whether your condition is true or false
// it is similar to while loop





// Example

let i = 10
do {
    console.log(i)
    i++
} while (i <= 5)

// Here even though the condition is false still it is running at-least once and logging the value of i





// Advantages of for loop over while and do while
// - for loop is of just 3 lines
// - after for loop ends, the i variable in it gets deleted as it is in that scope only but in case of while it is in the global scope so it will not delete






